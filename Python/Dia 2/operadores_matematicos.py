x = 6
y = 2
z = 7

print(f"{x} más {y} es igual a {x+y}")
print(f"{x} menos {y} es igual a {x-y}")
print(f"{x} por {y} es igual a {x*y}")
print(f"{x} entre {y} es igual a {x/y}")
print(f"{z} división al piso {y} es igual a {z//y}") #Con este doble "//" hace que el resultado de la división lo redon-
                                                     #dea para abajo
print(f"{z} modulo de {y} es igual a {z%y}") # Esto es el modulo de la división "%", o sea, el residuo de una división
                                             # Se suele usar para saber si un numero es par o impar
print(f"{x} elevado a la {y} potencia es igual a {x**y}") # Potencia de cualquier numero, usando "**"
print(f"La raiz cuadrada de {x} es {x**0.5}") # Cualquier numero con "**" por 0.5 es l2a raiz cuadrada del numero
