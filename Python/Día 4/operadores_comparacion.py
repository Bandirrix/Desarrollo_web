# Los operadores de comparacion son simbolos que nos permiten hacer comparación entre dos valores
# Lo que se genera a partir de estos es una valor booleano, o sea, sí o no se cumple la condición de comparación 

# == no es igual a = 
var1 = 10 # Un solo signo de igual es para asignar un valor
mi_bool = 10 == 25 # == hace comparacion de si son iguales los valores 
mi_bool = 5+5 == 18-8
mi_bool2 = 'blanco' == 'negro' #Sensitivo a las mayusculas y minusculas
mi_bool3 = '100' == 100 #Diferencia entre tipos de datos
print(mi_bool)
print(mi_bool2)
print(mi_bool3)

# != es para preguntar si son diferentes
mi_bool = 10 != 10
print(mi_bool)
mi_bool = 10 <= 10 # Menor o igual que 
print(mi_bool)
mi_bool = 10 >= 10 # Mayor o igual que 
print(mi_bool)
