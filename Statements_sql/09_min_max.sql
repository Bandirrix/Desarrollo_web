--MAX Y MIN

/*Se usa este tipo de consulta para pdoer obtener un valor minimo y maximo de un conjunto de valores numericos*/

SELECT MAX(age) FROM users;
/*Muestra el valor mas alto de un conjunto de valores, en este caso de la edad*/
-- Solo muestra el valor mas alto

SELECT MIN(age) FROM users;
/*Muestra el valor mas bajo de un conjunto de valores, en este caso de la edad*/
-- Solo muestra el valor mas bajo